//! @file
#ifndef IS_DIGIT_H
#define IS_DIGIT_H
/**
 * @brief Checks if a character is a digit (0-9)
 *
 * Determines whether a given character is a digit or not by checking
 * if it falls within the ASCII range for digits ('0' to '9').
 *
 * @param new_char The character to be checked
 * @return int Returns 1 if the character is a digit, otherwise returns 0
 */
int is_digit(char new_char);

#endif
